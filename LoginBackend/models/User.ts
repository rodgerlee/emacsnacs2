const mongose = require("mongoose");
const Schema = mongose.Schema;

const UserSchema = new Schema({
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

module.exports = { User: mongoose.model("users", UserSchema) };